package FirstDay;

import java.awt.Frame;

public class fourClass {
    public static void main(String[] args) {
        // Fram 作用是制造串口
        java.awt.Frame f = new Frame();
        // 设定弹窗的宽高
        f.setSize(800, 666);
        // 设定弹窗的标题
        f.setTitle("贪玩蓝月");
        // 设定弹窗的显示方式，true是显示，false是不显示
        f.setVisible(true);
    }
}
